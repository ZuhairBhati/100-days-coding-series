if char == 'A' or char == 'a':
    print('Vowel')
elif char == 'E' or char == 'e':
    print('Vowel')
elif char == 'I' or char == 'i':
    print('Vowel')
elif char == 'O' or char == 'o':
    print('Vowel')
elif char == 'U' or char == 'u':
    print('Vowel')
else:
    print('Consonant')

